
//Se instancian las funnciones de suma, resta, multiplicacion y division para el uso de una caluclador que sera testeada 
//por el framework de MochaJS
exports.suma = function(value1, value2) {
    return  value1 + value2;
  }

exports.resta = function(value1, value2) {
  return  value1 - value2;
}

exports.multiplicacion = function(value1, value2) {
  return  value1 * value2;
}

exports.division = function(value1, value2) {
  return  value1 / value2;
}